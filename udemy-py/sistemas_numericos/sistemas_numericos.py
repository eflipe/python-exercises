a = 10

# constructor init()
# especificamos la base
a = int('27', 10)  # base decimal
a = int('10111', 2)  # base binaria
a = int('27', 8)  # base octal
a = int('17', 16)  # base hexa
print(f'a: {a}')
a = 3.0
print(f'a: {a:.5f}')
a = float(10)
a = 3e6  # con 6 ceros
print(f'a: {a}')
a = 3e-5
print(f'a: {a:.5f}')
