'''
Proporciona el alto:
Proporciona el ancho:
Area: <area>
Perímetro: <perimetro>

Área: alto * ancho

Perímetro: (alto + ancho) * 2

'''

alto = int(input("Proporciona el alto: "))
ancho = int(input("Proporciona el ancho: "))

area = alto * ancho
perimetro = (alto + ancho) * 2

print("Area:", area)
print("Perimetro:", perimetro)
